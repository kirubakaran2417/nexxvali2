package decisionsbranchinglooping;

public class switchcases {

	public static void main(String[] args) {
		int day =3;

		switch (day) {
		case 1:
			System.out.println("Im monday");
		case 2:
			System.out.println("Im tuesday");
		case 3:
			System.out.println("Im wednesday");
			break;
		case 4:
			System.out.println("Im thursday");
		case 5:
			System.out.println("Im friday");
		case 6:
			System.out.println("Im saturday");
		case 7:
			System.out.println("Im sunday");
		}
	}

}
